# Combien d'handle contiennent un numéro ?
def nombre_de_handle_avec_numéro
    nombre=0
    @journaliste.each do |element|
        nombre += 1 if element.match?(/\d/)
    end
    puts "le nombre de handle qui contien un numero est : #{nombre}"
end
#nombre_de_handle_avec_numéro


# Combien d'handle contiennent les 4 lettres du prénom "aude" à la suite ? (s'il y a "Aude", ou "aUde", ou "AUDE", ça doit compter)

def nombre_de_handle_avec_aude
    nombre=0
    @journaliste.each do |element|
        nombre += 1 if element.match?(/aude/i)
    end
    puts "le nombre de handle qui contien aude est : #{nombre}"
end
#nombre_de_handle_avec_aude

# Combien commencent par une majuscule (première lettre juste après le @) ?

def nombre_de_handle_commensant_par_une_majuscule 
    nombre=0
    @journaliste.each do |element|
        nombre += 1 if element.match?(/@([A-Z][a-zA-Z0-9]*)/)
    end
    puts "le nombre de handle qui commencent par une majuscule est : #{nombre}"
end
#nombre_de_handle_commensant_par_une_majuscule 

# Combien contiennent au moins une majuscule ?
def nombre_de_handle_contient_une_majuscule 
    nombre=0
    @journaliste.each do |element|
        nombre += 1 if element.match?(/[A-Z]/)
    end
    puts "le nombre de handle qui contien au moins une majuscule est : #{nombre}"
end
#nombre_de_handle_contient_une_majuscule

# Combien y a-t-il de underscore _ dans tous les pseudos confondus ?
def nombre_de_underscore 
    nombre=0
    @journaliste.each do |element|
        nombre += element.count("_")
    end
    puts "le nombre de underscore _ dans tous les pseudos confondus  est : #{nombre}"
end
